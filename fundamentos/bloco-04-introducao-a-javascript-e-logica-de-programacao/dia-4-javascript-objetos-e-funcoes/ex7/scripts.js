function contador(array){
  let result = {};
  for(let i = 0; i < array.length; i += 1){
    const fruta = array[i];
    if(!result[fruta]){
      result[fruta] = 0;
    }
    result[fruta] += 1;
  }
  const quant = [];
  for (fruta in result){
    let mensagem = `${result[fruta]} ${fruta}`;
    if (result[fruta] > 1){
      mensagem += 's';
    }
    quant.push(mensagem);
  }
  console.log(`Sua cesta possui: ${quant.join(', ')}.`);
}
